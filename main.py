# function is a set of instructions grouped together
# function declaration is creating a function
# function invocation is using a function
def name(first_name, last_name):
    print(first_name)
    print(last_name)

name("Hosanna", "Bilewu")
name("Dami","Oluwasegun")

def div(a,b):
    return a/b
def multiplication(c,d):
    return c*d
def sub(e,f):
    return e-f
def add(g,h):
    return g+h

print(div(36,4))
print(multiplication(3,7))
print(sub(10,5))
print(add(23,16))
    
